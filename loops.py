"""
Basic loop examples in Python.
Demonstrates for-loops and while-loops for beginners.
"""

# Print numbers from 1 to 5 using a for-loop
for i in range(1, 6):
    print(i)

# Use a while-loop to count down from 3
count = 3
while count > 0:
    # Decrease count to avoid an infinite loop
    print(count)
    count -= 1

# Example output:
# 1
# 2
# 3
# 4
# 5
# 3
# 2
# 1

